# n = (n - 1) + (n - 2)
# n = 1 => 2
# n = 2 => 3

sub fibo {
    my ($n) = @_;
    return 0 if ($n == 0);
    return 2 if ($n == 1);
    return 3 if ($n == 2);
    fibo($n - 1) + fibo($n - 2);
}
print fibo(30);

